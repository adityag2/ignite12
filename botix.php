A Inclined Surface Climbing Robo Competition.<br />
Task:-<br />
Design and construct a remote controlled robot capable of running on Inclined Surface (at an angle of 10 deg.) and capable to run on irregular surface with or without Load (weight in cart).<br />
<br />
Dimensions and Fabrications<br />
1)The machine should fit in a box of dimension 30 cm x 30 cm x 30 cm (l*b*h) at every given point of the race. The external device used to control the machine such as remote control box or DC converter is not included in the size constraint.<br />
2) The bot has to be controlled by a wireless/wired remote control mechanism throughout the competition.<br />
3) The bot must not be made from Lego parts, or any readymade assembly kits.<br />
4) Remote control systems from toys might be used. Remote control systems available in the market may also be used.<br />
5) Aluminium Chassis or fiber glass or Plastic sheet or any other light in weight but strong material can be used for the construction.<br />
<br />
Robot Control Requirements<br />
1) The machine can be controlled wirelessly. Off board power supplies are allowed. Refer below for further details on battery and power.<br />
2) Remote control systems from toys might be used. Remote control systems available in the market may also be used.<br />
<br />
Motor specifications<br />
1) The boat can have an external D.C supply or an on board power supply to provide power to any mechanism requiring electric power.<br />
2) Only dc motor should be used for the power supply.<br />
<br />
Battery and Power<br />
