               Introduction:-<br />

Set your rudders, gun your propellers and cut through choppy waves in this one of a kind Radio/Manual controlled boat race event. <br />
Use your craftiness to make a simple RC/Manual sail boat and run it in a competition to outpace and outmaneuver your competitor.<br />
<br />
Task:-<br />

Design and construct a remote controlled robot capable of running on water surface and Hit the Ball and can hit the ball in Goal in minimum time.<br />
<br />
Specifications:-<br />

Dimensions and Fabrications<br />

1)The machine should fit in a box of dimension 30 cm x 30 cm x 30 cm (l*b*h) at every given point of the race. The external device used to control the machine such as remote control box or DC converter is not included in the size constraint.<br />

2) The boat has to be controlled by a wireless/wired remote control mechanism throughout the competition.<br />

3) The boat must not be made from Lego parts, or any readymade assembly kits.<br />

4) Remote control systems from toys might be used. Remote control systems available in the market may also be used.<br />

5) Balsa wood or fiber glass or any other light in weight but strong material can be used for the construction.<br /><br />

Robot Control Requirements<br />

1) The machine can be controlled wirelessly. Off board power supplies are allowed. Refer below for further details on battery and power.<br />

2) Remote control systems from toys might be used. Remote control systems available in the market may also be used.<br />
<br />
Motor specifications<br />

1) The boat can have an external D.C supply or an on board power supply to provide power to any mechanism requiring electric power.<br />

2) Only dc motor should be used for the power supply.<br />
<br />
Battery and Power<br />

1) The machine can be powered electrically only. Use of an IC engine in any form is not allowed. 
On board Batteries must be sealed, immobilized-electrolyte types (such as gel cells, lithium, NiCad, NiMH, or dry cells).<br />

2) Use of damaged, non-leak proof batteries may lead to disqualification. Maximum D.C power permitted is 24 Volts.<br />
<br />
Safety Rules:-<br />

Compliance with all event rules is mandatory. It is expected that competitors stay within the rules and procedures of their own accord and do not require constant policing<br />

1) The machine you build would be first checked for size and safety.<br />

2) The machine should not cause electrical power breakdown.<br />

3) The robot must not contain any combustible, corrosive, or otherwise dangerous materials.<br />

4) Explosive, compressive or expansive, either internal or external is not permitted.<br />

ANY ROBOT WHOSE STRATEGY OR OPERATION IS CONSIDERED DANGEROUS CAN BE DISQUALIFIED BY THE JUDGES AT THEIR DISCRETION.<br />

