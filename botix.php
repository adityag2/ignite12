General Guidelines<br />
The rules for design are very flexible in order to generate maximum technological creativity. The competition will evaluate each robot's performance in climbing a Inclined Surface constructed of different materials and obstacles of various dimensions. Each student group will have an opportunity to practice with the Arena prior to the competition. Judges will score each robot in five functional areas: Utility, navigation, versatility, speed, and innovation. The functional areas will specifically measure the robot's ability to complete the following tasks: <br />
Utility: Carry a load in proportion to its own body weight. <br />
Navigation: Complete the Task without touching obstacles on the Arena Surface.<br /> 
Versatility: Climb up surfaces composed of different materials and constructed in various shapes and sizes. <br />
Speed: Run up the full length of the Arena Surface from bottom to top in any manner as quickly as possible. <br />
Innovation: Demonstrate unique capabilities, innovative use of technology, general creativity, or style. <br />

The surface, as depicted, will be used to test the robot's performance in navigation, versatility, and speed. (The utility and innovation tasks will be performed independently.) <br />
The Arena is made of on plywood, and contains three sections composed of different materials and obstacles. <br />
The first section, closest to the floor, contain a wooden ramp (at 10 deg angle) surface, two obstacles.<br />
 The second section, in the middle of the wall, contains an rough surface and three obstacles.<br />
 The third section, at the top of the wall, contains an irregular sufaces, and two obstacles.<br />
