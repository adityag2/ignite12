                          Introduction <br></font>


Making the complicated simple is common place but making the simple complicated: awesomely complicated, that's creativity. Its not always about wrapping up events in a finite number of sequences but to see a simple step getting completed in a series of tedious steps is amusing.
CONTRAPTION is one of its kind occasion which places your primary technological innovation abilities like mechanics, electronics and physics to check. Here you need to confirm your mettle by opening your creativity and developing the most complicated components on the base of primary technological innovation concepts to execute a process.
Enough of holding your fire….Let your complicated minds to fly out like a FALCON in the sky of Engineering.<br>
<font color="brown">
                          Event: <br>

Event consists of two rounds:<br></font>

Round1: Demonstrate Ur Art!!<br>
Come and Scratch your brain and configure your view and represent it………….<br>
 In this round all the participants have to draw design of contraption on a paper on the spot. <br>
Round2: Goal Tracking!!<br>
Real time to show your talent by implementing your skills………..<br>
In this round the selected teams will make actual contraption model by the given equipments that was drawn on paper in 1st round. The teams will have to make contraption model of the problem by satisfying it with the given conditions. Selected team will be the final winner.
<br>
Additional audience round is there.<br>
<font color="brown>"
Problem Statement:<br></font>
Making use of Plastic ball, dominoes and a marble ball make a contraption starting from anyone of the above such that:<br>
•	Marble ball passes through a hole made in a cardboard.<br>
•	The plastic ball passes through a bowl full of water.<br>
•	Prepare a tower of playing cards and make it fall down.<br>
•	Show the dominos effect.<br>
<br>

Best of Luck!!<br>

<font color="brown">
Rules:<br></font>

1.	Maximum of 3 members are allowed in a team.<br>
2.	Each team will submit one paper drawing of the contraption on the spot.<br>
3.	A space of 4*4feet will be provided to each team.<br>
4.	50 entries are available so first come first serve.<br>
5.	Selected teams will go to second round for implementation.<br>
6.	5 teams will be selected for second round.<br>
7.	Equipments will be given by us.<br>
8.	Overall three trials will be given to show the complete contraption.<br>
9.	Minimum no of steps to achieve your goal is three.<br>
10.	The contraption must be safe to the satisfaction of the judges and coordinators.<br>
11.	All the four tasks should be completed in any order.<br>
12.	 A set up time of 15-20 minutes will be given before each trial. In addition to this addition time of 90 minutes will be given before their first attempt to construct the gadget.
 <br><br>
<font color="brown">
Judging Criteria:<br></font>
1.	Judgment will be based on the working model followed by all constraints and satisfying all conditions.<br>
2.	All conditions need to be fulfilled.<br>
3.	No of steps till reaching the goal.<br>
4.	Shape of domino effect used.<br>
5.	Originality of the ideas and innovativeness.<br>
6.	Cleanliness.<br>
7.	No. of trails.<br>
8.	Decision of judge will be the final.<br>

